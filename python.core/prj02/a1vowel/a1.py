def isVowel(x):
    if (
        x == 'A' or x == 'E' or x == 'I' or x == '0' or x == 'U' or
        x == 'a' or x == 'e' or x == 'i' or x == 'o' or x == 'u'
    ):
        print('vowel')
    else:
        print('consonant')
        return
isVowel('e')
isVowel('Z')
