package statements;

public class elseif {
    public static void main(String[] args) {
        float mark = 99;
        if (mark>85) {
            System.out.println("excellent");
        }else if(mark>75){
            System.out.println("very good");
        }else if(mark>50){
            System.out.println("good");
        }
    }
}
