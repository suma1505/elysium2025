package mathandWrapper;

public class MaxMinAbs {
    public static void main(String[] args) {
        int a = -10, b = 20;
        System.out.println("Max: " + Math.max(a, b));
        System.out.println("Min: " + Math.min(a, b));
        System.out.println("Absolute of " + a + ": " + Math.abs(a));
    }

}
